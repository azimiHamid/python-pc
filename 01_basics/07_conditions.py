# conditions in python: if elif else

temp = int(input("What is the temprature today? "))

if temp > 30:
    print("It's a hot day.")
    print("Drink plenty of water!")
elif temp > 20:
    print("It's a nice day.")
elif temp > 10:
    print("It's a bit cold.")
else:
    print("It's cold today!!")

